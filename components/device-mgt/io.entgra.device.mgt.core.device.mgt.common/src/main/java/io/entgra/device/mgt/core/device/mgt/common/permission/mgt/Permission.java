package io.entgra.device.mgt.core.device.mgt.common.permission.mgt;

/**
 * This class represents the information related to permission.
 */
public class Permission {

    private String name; // permission name
    private String path; // permission string
    private String url; // url of the resource
    private String method; // http method
    private String urlPattern;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getUrlPattern() {
        return urlPattern;
    }

    public void setUrlPattern(String urlPattern) {
        this.urlPattern = urlPattern;
    }
}
